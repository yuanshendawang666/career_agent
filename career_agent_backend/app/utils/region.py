def get_province_from_address_cpca(address: str) -> str:
    """
    使用 cpca 库从地址字符串中提取省份名称（备用方法）。

    Args:
        address (str): 地址字符串。

    Returns:
        str: 提取出的省份名称，若解析失败或地址为空则返回空字符串。
    """
    if not address:
        return ""
    try:
        df = cpca.transform([address])
        province = df.iloc[0]["省"]
        return province if province else ""
    except Exception:
        # 解析失败时静默返回空字符串
        return ""


def get_region_from_address(address: str) -> str:
    """
    根据地址字符串确定所属区域（东部、中部、西部、东北）。

    解析流程：
        1. 优先使用城市映射表（CITY_REGION_MAP）快速匹配。
        2. 若城市匹配失败，则使用 cpca 库解析出省份，再查询省份映射表（REGION_MAP）。
        3. 若仍无法识别，返回 "未知"。

    Args:
        address (str): 地址字符串。

    Returns:
        str: 区域名称，可能为 "东部"、"中部"、"西部"、"东北" 或 "未知"。
    """
    if not address:
        return "未知"

    # 1. 优先尝试城市匹配（快速且准确）
    for city, region in CITY_REGION_MAP.items():
        if city in address:
            return region

    # 2. 城市匹配失败，使用 cpca 解析省份
    province = get_province_from_address_cpca(address)
    if province:
        return REGION_MAP.get(province, "未知")

    # 3. 完全无法识别
    return "未知"